package it.eng.myportal.exception;

public class ServiziLavoratoreException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3369003432911197183L;

	public ServiziLavoratoreException() {
		super();
	}

	public ServiziLavoratoreException(String message) {
		super(message);
	}

	public ServiziLavoratoreException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServiziLavoratoreException(Throwable arg0) {
		super(arg0);
	}
}
