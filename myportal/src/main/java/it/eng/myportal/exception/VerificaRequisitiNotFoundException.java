package it.eng.myportal.exception;

public class VerificaRequisitiNotFoundException extends Exception {

	private static final long serialVersionUID = -2892858001769540993L;

	public VerificaRequisitiNotFoundException() {
		super();
	}

	public VerificaRequisitiNotFoundException(String message) {
		super(message);
	}

	public VerificaRequisitiNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public VerificaRequisitiNotFoundException(Throwable arg0) {
		super(arg0);
	}
}
