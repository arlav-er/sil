package it.eng.myportal.exception;

public class ConfermaPeriodicaException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2892858001769540993L;

	public ConfermaPeriodicaException() {
		super();
	}

	public ConfermaPeriodicaException(String message) {
		super(message);
	}

	public ConfermaPeriodicaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ConfermaPeriodicaException(Throwable arg0) {
		super(arg0);
	}
}
