package it.eng.myportal.exception;

public class StatoOccupazionaleException extends Exception {

	private static final long serialVersionUID = -7970667856403037721L;

	public StatoOccupazionaleException() {
		super();
	}

	public StatoOccupazionaleException(String message) {
		super(message);
	}

	public StatoOccupazionaleException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StatoOccupazionaleException(Throwable arg0) {
		super(arg0);
	}

}
