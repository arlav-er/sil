package it.eng.myportal.exception;

public class StipulaDidException extends Exception {
	private static final long serialVersionUID = -7970667856403037721L;

	public StipulaDidException() {
		super();
	}

	public StipulaDidException(String message) {
		super(message);
	}

	public StipulaDidException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StipulaDidException(Throwable arg0) {
		super(arg0);
	}
}
