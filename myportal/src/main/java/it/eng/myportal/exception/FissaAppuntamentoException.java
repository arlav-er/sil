package it.eng.myportal.exception;

public class FissaAppuntamentoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6870384475323390216L;

	public FissaAppuntamentoException() {
		super();
	}

	public FissaAppuntamentoException(String message) {
		super(message);
	}

	public FissaAppuntamentoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FissaAppuntamentoException(Throwable arg0) {
		super(arg0);
	}

}
