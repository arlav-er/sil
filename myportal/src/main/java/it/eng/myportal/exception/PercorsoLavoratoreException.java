package it.eng.myportal.exception;

public class PercorsoLavoratoreException extends Exception {

	private static final long serialVersionUID = -7970667856403037721L;

	public PercorsoLavoratoreException() {
		super();
	}

	public PercorsoLavoratoreException(String message) {
		super(message);
	}

	public PercorsoLavoratoreException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PercorsoLavoratoreException(Throwable arg0) {
		super(arg0);
	}

}
