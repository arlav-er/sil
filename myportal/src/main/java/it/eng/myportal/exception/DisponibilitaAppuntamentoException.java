package it.eng.myportal.exception;

public class DisponibilitaAppuntamentoException extends Exception {


	/**
	 * 
	 */
	private static final long serialVersionUID = -7162527404833600386L;

	public DisponibilitaAppuntamentoException() {
		super();
	}

	public DisponibilitaAppuntamentoException(String message) {
		super(message);
	}

	public DisponibilitaAppuntamentoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DisponibilitaAppuntamentoException(Throwable arg0) {
		super(arg0);
	}

}
