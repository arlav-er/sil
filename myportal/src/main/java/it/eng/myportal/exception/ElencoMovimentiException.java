package it.eng.myportal.exception;

public class ElencoMovimentiException extends Exception {

	private static final long serialVersionUID = -7970667856403037721L;

	public ElencoMovimentiException() {
		super();
	}

	public ElencoMovimentiException(String message) {
		super(message);
	}

	public ElencoMovimentiException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ElencoMovimentiException(Throwable arg0) {
		super(arg0);
	}

}
